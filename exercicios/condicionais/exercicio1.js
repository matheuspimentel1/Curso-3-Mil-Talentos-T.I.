// Criando uma função

function comparaNumeros(numero1, numero2) {
    if (numero1 === numero2){
        return "Os números são iguais! :D"
    }else{
        return "Os números são diferentes :(((("
    }
}

// Definir as variáveis de entrada (numero1 e numero2)

let numero1 = 20
let numero2 = 20

// Chamada da função
resultado = comparaNumeros(numero1, numero2)
console.log(resultado)
